#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*dup;
	unsigned long	i;

	i = 0;
	while (s1[i])
		i++;
	if (!(dup = (char *)malloc((i + 1) * sizeof(char))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
